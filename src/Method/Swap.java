package Method;

public class Swap {
    public static void main(String args[]){
        //swap-exchange numbers;
        int a=20;
        int b=40;

        //swap;
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("a ="+a);
        System.out.println("b ="+b);
    }
}
